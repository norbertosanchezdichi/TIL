# PEMDAS is followed for calculations.

# Exponentiation
print("2^2 = " + str(2 ** 2))

# Division always returns a float
print ("1 / 2 = " + str(1 / 2))

# For integer division, use double forward-slashes
print ("1 // 2 = " + str(1 // 2))

# Modulo is like division except it returns the remainder
print ("10 % 3 = " + str(10 % 3))